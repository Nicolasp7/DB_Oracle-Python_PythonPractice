# AND
number1 = 10
number2 = 10
if number1 == 10 and number2 == 10:
    print('Both values are the same')
else:
    print('Both values are not the same')
# OR
number1 = 50
number2 = 8
if number1 == 10 or number2 == 10:
    print('At least one value is 10')
else:
    print('No values with 10')
